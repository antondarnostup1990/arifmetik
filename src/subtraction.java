public class subtraction {
    static int a = 15;
    static int b = 5;
    static int c = a - b;  // 10
    static int d = 4 - a;  // -11

    public static void main(String[] args) {
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }

}
