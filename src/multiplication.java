public class multiplication {
        static int a = 25;
        static int b = 18;
        static int c = a * b;  // 450
        static int d = b * 5;  // 90

    public static void main(String[] args) {
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }

}
